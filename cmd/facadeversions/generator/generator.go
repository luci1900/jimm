package generator

import (
	"bytes"
	"fmt"
	"go/format"
	"sort"
	"strings"
)

// Options controls the generated Go source.
//
// PackageName and VarName are required.
// Source is included in the file header for traceability.
//
// The generated output is gofmt'd.
type Options struct {
	PackageName string
	VarName     string
	Source      string
}

// Generate renders a Go source file containing a map[string][]int.
func Generate(facades map[string][]int, opts Options) ([]byte, error) {
	if strings.TrimSpace(opts.PackageName) == "" {
		return nil, fmt.Errorf("PackageName must be set")
	}
	if strings.TrimSpace(opts.VarName) == "" {
		return nil, fmt.Errorf("VarName must be set")
	}

	var b bytes.Buffer
	b.WriteString("// Code generated by cmd/facadeversions; DO NOT EDIT.\n")
	if strings.TrimSpace(opts.Source) != "" {
		b.WriteString("//\n")
		b.WriteString("// Source: ")
		b.WriteString(opts.Source)
		b.WriteString("\n")
	}
	b.WriteString("\n")
	b.WriteString("package ")
	b.WriteString(opts.PackageName)
	b.WriteString("\n\n")

	b.WriteString("// ")
	b.WriteString(opts.VarName)
	b.WriteString(" is a snapshot of the facade versions registered by this server.\n")
	b.WriteString("//\n")
	b.WriteString("// It is generated from runtime registration (see SupportedFacades).\n")
	b.WriteString("var ")
	b.WriteString(opts.VarName)
	b.WriteString(" = map[string][]int{\n")

	names := make([]string, 0, len(facades))
	for name := range facades {
		names = append(names, name)
	}
	sort.Strings(names)

	for _, name := range names {
		versions := append([]int(nil), facades[name]...)
		sort.Ints(versions)

		b.WriteString("\t")
		b.WriteString(quote(name))
		b.WriteString(": []int{")
		for i, v := range versions {
			if i > 0 {
				b.WriteString(", ")
			}
			fmt.Fprintf(&b, "%d", v)
		}
		b.WriteString("},\n")
	}

	b.WriteString("}\n")

	out, err := format.Source(b.Bytes())
	if err != nil {
		// If go/format fails, return the unformatted source to make debugging easier.
		return b.Bytes(), fmt.Errorf("format generated source: %w", err)
	}
	return out, nil
}

func quote(s string) string {
	// Avoid pulling in strconv just for Quote; facade names should be simple.
	replacer := strings.NewReplacer("\\", "\\\\", "\"", "\\\"")
	return "\"" + replacer.Replace(s) + "\""
}
